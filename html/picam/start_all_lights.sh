#!/bin/bash

#Activate Lights following the instructions on https://www.pi-supply.com/bright-pi-v1-0-code-examples/

i2cset -y 1 0x70 0x00 0xff #turn on all lights
i2cset -y 1 0x70 0x09 0x0f #turn up gain

#Turn up brightness
i2cset -y 1 0x70 0x02 0x32
i2cset -y 1 0x70 0x04 0x32
i2cset -y 1 0x70 0x05 0x32
i2cset -y 1 0x70 0x07 0x32
i2cset -y 1 0x70 0x01 0x32
i2cset -y 1 0x70 0x03 0x32
i2cset -y 1 0x70 0x06 0x32
i2cset -y 1 0x70 0x08 0x32

#Done